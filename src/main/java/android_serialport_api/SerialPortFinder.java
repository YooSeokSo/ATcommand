package android_serialport_api;

import java.io.DataOutputStream;
import java.io.File;
import java.io.FileReader;
import java.io.IOException;
import java.io.LineNumberReader;
import java.util.Iterator;
import java.util.Vector;

import android.util.Log;

public class SerialPortFinder {

	public class Driver {
		public Driver(String name, String root) {
			mDriverName = name;
			mDeviceRoot = root;
		}
		private String mDriverName;
		private String mDeviceRoot;
		Vector<File> mDevices = null;
		public Vector<File> getDevices() {
			if (mDevices == null) {
				mDevices = new Vector<File>();
				File dev = new File("/dev");
				File[] files = dev.listFiles();
				int i;
				for (i=0; i<files.length; i++) {
					if (files[i].getAbsolutePath().startsWith(mDeviceRoot)) {
						Log.d(TAG, "Found new device: " + files[i]);
						mDevices.add(files[i]);
					}
				}
			}
			return mDevices;
		}
		public String getName() {
			return mDriverName;
		}
	}

	private static final String TAG = "SerialPort";

	private Vector<Driver> mDrivers = null;
	private File procfile = null;
	Vector<Driver> getDrivers() throws IOException {
		Log.d(TAG,"getDrivers");
		if (mDrivers == null) {
			mDrivers = new Vector<Driver>();
			procfile = new File("/proc/tty/drivers");

			if (!procfile.canRead() || !procfile.canWrite()) {
				Log.d(TAG,"can`t read drivers");
				try {
					Log.d(TAG,"In try");
					/* Missing read/write permission, trying to chmod the file */

					Process su = Runtime.getRuntime().exec("su");
					DataOutputStream outputStream = new DataOutputStream(su.getOutputStream());
					String cmd = "chmod 666 " + procfile.getAbsolutePath() + "\n";
					outputStream.write(cmd.getBytes());
					outputStream.flush();
					outputStream.writeBytes("exit\n");
					outputStream.flush();
					if(su.waitFor() != 0){Log.d(TAG,"waitFor error");};
					if (!procfile.canRead()
							|| !procfile.canWrite()) {
						throw new SecurityException();
					}
					Log.d(TAG,"Read sucsess");
				} catch (Exception e) {
					Log.d(TAG,"Fail to read");
					e.printStackTrace();
					throw new SecurityException();
				}
			}
			Log.d(TAG,"finish permission");
			LineNumberReader r = new LineNumberReader(new FileReader(procfile));
			String l;
			while((l = r.readLine()) != null) {
				// Issue 3:
				// Since driver name may contain spaces, we do not extract driver name with split()
				String drivername = l.substring(0, 0x15).trim();
				String[] w = l.split(" +");
				if ((w.length >= 5) && (w[w.length-1].equals("serial"))) {
					Log.d(TAG, "Found new driver " + drivername + " on " + w[w.length-4]);
					mDrivers.add(new Driver(drivername, w[w.length-4]));
				}
			}
			r.close();
		}
		return mDrivers;
	}

	public String[] getAllDevices() {
		Vector<String> devices = new Vector<String>();
		// Parse each driver
		Iterator<Driver> itdriv;
		try {
			itdriv = getDrivers().iterator();
			while(itdriv.hasNext()) {
				Driver driver = itdriv.next();
				Iterator<File> itdev = driver.getDevices().iterator();
				while(itdev.hasNext()) {
					String device = itdev.next().getName();
					String value = String.format("%s (%s)", device, driver.getName());
					devices.add(value);
				}
			}
		} catch (IOException e) {
			e.printStackTrace();
		}
		return devices.toArray(new String[devices.size()]);
	}

	public String[] getAllDevicesPath() {
		Vector<String> devices = new Vector<String>();
		// Parse each driver
		Iterator<Driver> itdriv;
		try {
			itdriv = getDrivers().iterator();
			while(itdriv.hasNext()) {
				Driver driver = itdriv.next();
				Iterator<File> itdev = driver.getDevices().iterator();
				while(itdev.hasNext()) {
					String device = itdev.next().getAbsolutePath();
					devices.add(device);
				}
			}
		} catch (IOException e) {
			e.printStackTrace();
		}
		return devices.toArray(new String[devices.size()]);
	}
}
